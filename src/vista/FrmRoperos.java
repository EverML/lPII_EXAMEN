    public void refresh() {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Id");
        modelo.addColumn("Marca");
        modelo.addColumn("Tipo Madera");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Dimensiones");
        modelo.addColumn("Precio");

        roperos = roperoControl.listar("");

        Object[] fila = new Object[6];

        for (Ropero r : roperos) {
            fila[0] = r.getId();
            fila[1] = r.getMarca();
            fila[2] = r.getTipoMadera();
            fila[3] = r.getDescripcion();
            fila[4] = r.getDimension();
            fila[5] = r.getPrecio();

            modelo.addRow(fila);
        }

        tblPrincipal.setModel(modelo);

        limpiar();

    }

    private void limpiar() {
        txtMarca.setText("");
        txtTipoMadera.setText("");
        txtDescripcion.setText("");
        txtDimension.setText("");
        txtPrecio.setText("");

        lblId.setText("");
        btnAccionPrincipal.setText("Guardar");

    }
