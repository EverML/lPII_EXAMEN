    private void btnAccionPrincipalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccionPrincipalActionPerformed

        Integer id = null;
        String marca = txtMarca.getText();
        String tipoMadera = txtTipoMadera.getText();
        String descripcion = txtDescripcion.getText();
        String dimension = txtDimension.getText();
        String precioStr = txtPrecio.getText();
        BigDecimal precio = BigDecimal.ZERO;

        String mensaje = "";

        try {
            precio = new BigDecimal(precioStr);

            if (lblId.getText() != null && !lblId.getText().isEmpty()) {
                id = Integer.parseInt(lblId.getText());
            }

            ropero = new Ropero(id, marca, tipoMadera, descripcion, dimension, precio);

            if (ropero.getId() == null) {
                //INSERTAR
                roperoControl.insertar(ropero);
                mensaje = "guardado";
            } else {
                //modificar
                roperoControl.modificar(ropero);
                mensaje = "modificado";
            }

            this.refresh();

            JOptionPane.showMessageDialog(null, String.format("Se ha %s con exito", mensaje));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Precio no valido");
        }


    }//GEN-LAST:event_btnAccionPrincipalActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        Integer filaNum = tblPrincipal.getSelectedRow();

        if (filaNum < 0) {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un registro");
        } else {

            DefaultTableModel modelo = (DefaultTableModel) tblPrincipal.getModel();

            Integer id = (Integer) modelo.getValueAt(filaNum, 0);

            roperos = roperoControl.listar(String.format("WHERE id= %d ", id));

            ropero = roperos.get(0);

            limpiar();

            lblId.setText(ropero.getId().toString());

            txtMarca.setText(ropero.getMarca());
            txtTipoMadera.setText(ropero.getTipoMadera());
            txtDescripcion.setText(ropero.getDescripcion());
            txtDimension.setText(ropero.getDimension());
            txtPrecio.setText(ropero.getPrecio().toPlainString());

            btnAccionPrincipal.setText("Modificar");

        }


    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        Integer filaNum = tblPrincipal.getSelectedRow();

        if (filaNum < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        } else {

            int input = JOptionPane.showConfirmDialog(null, "Desea eliminar el registro?");

            if (input == 0) {
                eliminarRopero(filaNum);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void eliminarRopero(Integer filaNum) {

        DefaultTableModel modelo = (DefaultTableModel) tblPrincipal.getModel();
        Integer id = (Integer) modelo.getValueAt(filaNum, 0);
        roperos = roperoControl.listar(String.format("WHERE id= %d ", id));
        ropero = roperos.get(0);

        roperoControl.eliminar(ropero);
        
        this.refresh();
    }

